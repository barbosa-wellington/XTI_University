public class Math {
    
    
    /** 
     * @param one
     * @param two
     * @return the sum of both numbers 
     */
    int graterThan(int one, int two){
        
        
        if (one > two){
        return one;
        } else {
        return two;
        }
    }
    
    /** 
     * @param one
     * @param two
     * @return the sum of both numbers 
     */
    double sumupNumber(double one, double two){
        
        return one + two;
       
    }
    
}
